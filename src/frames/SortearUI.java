    private void inicializaListaPresenca() throws FileNotFoundException, IOException{
        int rowCounter=0;
        int count = 0;
        int indiceEquipe = 0;
        String indice="";
            FileReader leituraEquipes = new FileReader(dirCompeticao.concat("/listaEquipes/equipes.txt"));
            BufferedReader buff = new BufferedReader(leituraEquipes);
                    // PEGA OS INDICES DAS EQUIPES SORTEADAS E COMPARA PRA VERIFICAR QUAIS SÃO AS EQUIPES                           
                        while(buff.ready() && rowCounter<=24){
                            String linha = buff.readLine(); 
                            String splitLinha[] = linha.split(":");
                            tabelaPresenca.setValueAt(splitLinha[1], rowCounter, 0);
                            indice = splitLinha[0];       
                            rowCounter++;
                            count++;
                        }       
                        // Evita NullException
                        for(int i = 0; i<=24;i++){
                            tabelaPresenca.setValueAt((boolean) false, i, 1);
                        }
                        if(count>9){
                        numeroTotalEquipes = Integer.parseInt(indice.substring(0,2));
                        } else {
                        numeroTotalEquipes = Integer.parseInt(indice.substring(0,1));
                        }
                        
    }
    
    private void sorteio(){
        for(int row=0; row<numeroTotalEquipes;row++){
            if((boolean) tabelaPresenca.getValueAt(row, 1)==true){
                listaSorteioBateria.add((String) tabelaPresenca.getValueAt(row, 0));
            }
        }
        Collections.shuffle(listaSorteioBateria); 
    }
    

