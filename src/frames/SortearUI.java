    private void btnresetaBateriaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnresetaBateriaActionPerformed
        // TODO add your handling code here:

        Sorteio sortear = new Sorteio();
        boolean continua = !(sortear.testaArquivo());
            if(continua){
                int selectedOption = JOptionPane.showConfirmDialog(null, 
                    "Deseja Realizar um Novo sorteio?", 
                    "Finalizar", 
                    JOptionPane.YES_NO_OPTION); 
            if (selectedOption == JOptionPane.YES_OPTION) {
                    DefaultTableModel table = (DefaultTableModel) tabelaEquipes.getModel();
                    for(int i = 0; i<=29;i++){
                            tabelaEquipes.setValueAt("", i, 0);
                        }
                    listaSorteioBateria.clear();
                    listaSorteioFinalistas.clear();
                    btnSorteio.setEnabled(true);
                    JOptionPane.showMessageDialog(null,"Novo sorteio já pode ser realizado!");
            }
            }else{
            JOptionPane.showMessageDialog(rootPane, "Não existem equipes cadastradas!");
            }
    }//GEN-LAST:event_btnresetaBateriaActionPerformed

