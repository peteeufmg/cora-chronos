    private void preencheFinalistas() throws FileNotFoundException, IOException{
            int row = 0;
            FileReader leituraEquipes = new FileReader(dirCompeticao.concat("/listaEquipes/equipes.txt"));
            BufferedReader buff = new BufferedReader(leituraEquipes);  
                    while(buff.ready()){
                       boolean okFinalista;
                       String linha = buff.readLine();     
                       String splitLinha[] = linha.split(":");                     
                       int simFinalista = Integer.parseInt(splitLinha[4].substring(1, 2));
                       if(simFinalista==1){
                           okFinalista = true;
                       } else {
                           okFinalista=false;
                       }
                       if(okFinalista){
                           tabelaPresenca.setValueAt(splitLinha[1],row,0);
                           row++;
                       }
                       for(int i = 0; i<=24;i++){
                            tabelaPresenca.setValueAt((boolean) false, i, 1);
                        }
                    }
    }
